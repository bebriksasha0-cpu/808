// Fallback to English for missing translations
const getTranslation = (lang, key) => {
  return translations[lang]?.[key] || translations.en[key] || key
}

export function LanguageProvider({ children }) {
  const [country, setCountry] = useState(() => {
    if (typeof window !== 'undefined') {
      const saved = localStorage.getItem('country')
      return saved ? JSON.parse(saved) : countries[0]
    }
    return countries[0]
  })

  useEffect(() => {
    localStorage.setItem('country', JSON.stringify(country))
    document.documentElement.lang = country.lang
  }, [country])

  const t = (key) => getTranslation(country.lang, key)

  const changeCountry = (countryCode) => {
    const newCountry = countries.find(c => c.code === countryCode)
    if (newCountry) {
      setCountry(newCountry)
    }
  }

  return (
    <LanguageContext.Provider value={{ country, countries, changeCountry, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider')
  }
  return context
}
